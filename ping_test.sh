#!/bin/bash
cat << EOF
##########################################################################################################
############This script is to ping all the ip addrs to show which is alive.###############################
##########################################################################################################
EOF


for i in `seq 1 25`;do
	if ping 10.148.60.$i -c 1 >/dev/null 2>&1;then
		echo "Host 10.148.60.$i is up!"
	else
		echo "Host 10.148.60.$i is not up!"
	fi
done
